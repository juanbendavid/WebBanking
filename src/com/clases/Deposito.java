public class Deposito {
    private Cuenta cuenta;
    private String fecha;
    private String hora;
    private int monto;

    public Deposito(Cuenta cuenta, String fecha, String hora, int monto) {
        this.cuenta = cuenta;
        this.fecha = fecha;
        this.hora = hora;
        this.monto = monto;
    }

    public Cuenta getCuenta() {
        return cuenta;
    }

    public void setCuenta(Cuenta cuenta) {
        this.cuenta = cuenta;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public int getMonto() {
        return monto;
    }

    public void setMonto(int monto) {
        this.monto = monto;
    }
    
    
}
